    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    // Obt√©n los valores de los campos de entrada
    double caudalAspersor = Double.parseDouble(jTextField2.getText());
    int cantidadAspersores = (int) jSpinner1.getValue();
    int radioAlcance = (int) jSpinner5.getValue();
    int tiempo = Integer.parseInt(jTextField1.getText());

    // Crea un objeto Aspersor con los valores obtenidos
    Aspersor aspersor = new Aspersor(radioAlcance, caudalAspersor);
    double cantidadAgua = cantidadAspersores * aspersor.calcularPluviometria() * tiempo;
    double distanciaEntreAspersores = Math.sqrt(cantidadAspersores) * radioAlcance;

    // Calcula la pluvimetria
    double pluvimetria = aspersor.calcularPluviometria();
    
    // Formatea los valores con dos decimales
    DecimalFormat df = new DecimalFormat("#.##");
    cantidadAgua = Double.parseDouble(df.format(cantidadAgua));
    distanciaEntreAspersores = Double.parseDouble(df.format(distanciaEntreAspersores));
    pluvimetria = Double.parseDouble(df.format(pluvimetria));

    // Crea una instancia de la ventana Resultados
    Resultados resultadosWindow = new Resultados(cantidadAgua, distanciaEntreAspersores, pluvimetria);

    // Oculta la ventana Vistainicio y muestra la ventana Resultados
    this.dispose();
    resultadosWindow.setVisible(true);


    }//GEN-LAST:event_jButton1ActionPerformed

